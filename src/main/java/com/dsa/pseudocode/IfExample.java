
package com.dsa.pseudocode;

public class IfExample {
    /** 
     * start
     * input sal
     * if sal > 10000
     * 	sal = sal + 20000
     * 	output "sal"
     * else
     * 	sal = sal + 1000
     * 	outprint "sal"
     * exit
     */
    static void calculateBonus(int sal) {
	if(sal > 10000)
	    System.out.println(sal = sal + 200);
	else
	    System.out.println(sal = sal + 100);
    }
    
    public static void main(String[] args) {
	calculateBonus(9543);
    }
}
